import turtle
x=turtle.Turtle()
x.color("blue","green")
x.begin_fill()
x.forward(100)
x.left(90)
x.forward(100)
x.left(90)
x.forward(100)
x.left(90)
x.forward(100)
x.penup()
x.forward(100)
x.forward(100)
x.left(90)
x.pendown()
x.forward(100)
x.left(90)
x.forward(100)
x.left(90)
x.forward(100)
x.left(90)
x.forward(100)
x.end_fill()
turtle.done()